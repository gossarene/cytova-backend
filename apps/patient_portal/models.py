"""
Cytova — Patient Portal models (global, public schema)

Three rows together describe one patient's portal identity:

  - ``PatientAccount``  — credentials + lifecycle state (login).
  - ``PatientProfile``  — human-readable profile + the global Cytova
                          Patient ID.
  - ``PatientConsent``  — append-only legal-acceptance record.

These tables intentionally live in the ``public`` schema and are NOT
installed in any lab tenant schema. See ``apps/patient_portal/__init__.py``
for the full architecture rationale.
"""
from __future__ import annotations

import uuid

from django.contrib.auth.models import AbstractBaseUser
from django.db import models
from django.utils import timezone

from .managers import PatientAccountManager


class PatientAccount(AbstractBaseUser):
    """
    Global Cytova patient login. Email is the credential; password is
    hashed via Django's ``set_password`` (same KDF used everywhere else
    in the project).

    Notes
    -----
    - ``PermissionsMixin`` is intentionally omitted: patients do not
      carry Django auth groups / permissions, and pulling the mixin in
      would attach M2M tables to ``auth_*`` models we don't want
      coupled to a future dedicated schema.
    - This model is NOT ``AUTH_USER_MODEL`` (that role belongs to
      ``apps.users.StaffUser`` in tenant schemas). Patient
      authentication will use a dedicated auth backend keyed on
      ``email``; staff JWT auth is unaffected.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    email = models.EmailField(unique=True, db_index=True)
    email_verified_at = models.DateTimeField(null=True, blank=True)
    is_active = models.BooleanField(default=True, db_index=True)

    created_at = models.DateTimeField(default=timezone.now, db_index=True)
    updated_at = models.DateTimeField(auto_now=True)
    # ``last_login`` is provided by ``AbstractBaseUser``.

    USERNAME_FIELD = 'email'
    REQUIRED_FIELDS: list[str] = []

    objects = PatientAccountManager()

    class Meta:
        # Explicit table name keeps the public schema readable and
        # decouples the table identity from any future Django app
        # rename. The ``patient_portal_`` prefix matches the
        # forthcoming dedicated schema's namespace.
        db_table = 'patient_portal_account'
        verbose_name = 'Patient Account'
        verbose_name_plural = 'Patient Accounts'
        ordering = ['-created_at']

    def __str__(self) -> str:
        return f'PatientAccount<{self.email}>'

    # --- Helpers -----------------------------------------------------

    def mark_email_verified(self) -> None:
        """Idempotent — only writes when the row hasn't been verified yet."""
        if self.email_verified_at is None:
            self.email_verified_at = timezone.now()
            self.save(update_fields=['email_verified_at', 'updated_at'])


class PatientProfile(models.Model):
    """
    Human-readable identity tied to a portal account.

    The ``cytova_patient_id`` is the value patients share verbally with
    a lab receptionist or quote on a paper form — it must be unique,
    visually unambiguous (no 0/1/I/O), and short enough to dictate over
    a phone. Generated by
    ``apps.patient_portal.id_generator.generate_cytova_patient_id``.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    account = models.OneToOneField(
        PatientAccount,
        on_delete=models.CASCADE,
        related_name='profile',
    )
    cytova_patient_id = models.CharField(
        max_length=12,
        unique=True,
        db_index=True,
        help_text='Public-facing Cytova Patient ID (CV-XXXX-XXXX).',
    )

    first_name = models.CharField(max_length=100)
    last_name = models.CharField(max_length=100)
    date_of_birth = models.DateField()
    phone = models.CharField(max_length=50, blank=True, default='')

    created_at = models.DateTimeField(default=timezone.now, db_index=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        db_table = 'patient_portal_profile'
        verbose_name = 'Patient Profile'
        verbose_name_plural = 'Patient Profiles'

    def __str__(self) -> str:
        return f'{self.cytova_patient_id} · {self.first_name} {self.last_name}'

    @property
    def full_name(self) -> str:
        return f'{self.first_name} {self.last_name}'.strip()


class PatientConsent(models.Model):
    """
    Append-only record of a patient's acceptance of the Terms of Service
    and Privacy Policy. Versions are snapshotted from settings at the
    moment of acceptance, so updates to the running settings never
    rewrite history.

    The IP address and user agent are recorded for audit-trail purposes
    (regulator inquiries about consent provenance). Rows are never
    updated or deleted in normal operation; renewing consent creates a
    new row.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    account = models.ForeignKey(
        PatientAccount,
        on_delete=models.CASCADE,
        related_name='consents',
    )
    terms_version = models.CharField(max_length=20)
    privacy_version = models.CharField(max_length=20)
    accepted_at = models.DateTimeField(default=timezone.now, db_index=True)
    ip_address = models.GenericIPAddressField(null=True, blank=True)
    user_agent = models.CharField(max_length=500, blank=True, default='')

    class Meta:
        db_table = 'patient_portal_consent'
        verbose_name = 'Patient Consent'
        verbose_name_plural = 'Patient Consents'
        ordering = ['-accepted_at']
        indexes = [
            models.Index(fields=['account', '-accepted_at']),
        ]

    def __str__(self) -> str:
        return (
            f'Consent<{self.account_id} terms={self.terms_version} '
            f'privacy={self.privacy_version} at={self.accepted_at:%Y-%m-%d}>'
        )


class PatientEmailVerificationToken(models.Model):
    """
    Single-use email-verification token for a portal account.

    The plaintext token is sent to the patient via email and never
    persisted — only the SHA-256 hash is stored (via
    ``common.utils.crypto.hash_token``). On verification:

    - the row is matched by hash + ``is_used=False`` + ``expires_at > now``
    - ``account.email_verified_at`` is stamped
    - the consumed token is marked ``is_used=True`` + ``used_at``
    - any other outstanding tokens for the same account are invalidated

    Mirrors the staff-side ``apps.users.PasswordResetToken`` shape so
    the same hashing / single-use / IP-capture invariants apply.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    account = models.ForeignKey(
        PatientAccount,
        on_delete=models.CASCADE,
        related_name='email_verification_tokens',
    )
    token_hash = models.CharField(max_length=64, unique=True, db_index=True)
    created_at = models.DateTimeField(default=timezone.now)
    expires_at = models.DateTimeField()
    is_used = models.BooleanField(default=False, db_index=True)
    used_at = models.DateTimeField(null=True, blank=True)
    created_by_ip = models.GenericIPAddressField(null=True, blank=True)

    class Meta:
        db_table = 'patient_portal_email_verification_token'
        verbose_name = 'Patient Email Verification Token'
        verbose_name_plural = 'Patient Email Verification Tokens'
        indexes = [
            models.Index(fields=['account', 'is_used']),
        ]

    def __str__(self) -> str:
        return (
            f'PatientEmailVerificationToken<account={self.account_id} '
            f'used={self.is_used} expires={self.expires_at:%Y-%m-%d %H:%M}>'
        )


