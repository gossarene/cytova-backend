class SharedResultSourceType(models.TextChoices):
    """Where the shared result originated. ``DIRECT`` means the lab ran
    the analysis themselves and is sharing the result with the patient.
    ``PARTNER`` means the request was received from a partner clinic /
    referring lab — captured separately so future analytics can split
    direct vs. partner-driven sharing without inferring it from text."""
    DIRECT = 'DIRECT', 'Direct'
    PARTNER = 'PARTNER', 'Partner'


class PatientSharedChannel(models.TextChoices):
    """Channel through which a result version reached the patient.

    Values map 1:1 to product flows:

    - ``CYTOVA``     — Notify Cytova (account-bound share, written here in
                       Phase 1).
    - ``EMAIL``      — Notify-by-email path that creates a portal-visible
                       version (reserved; no writer in Phase 1).
    - ``SHARE_LINK`` — Secure share link that materialises a portal entry
                       only when redeemed by an authenticated patient
                       (reserved; no writer in Phase 1).
    - ``MANUAL``     — Staff-driven manual share / backfill (reserved).

    Stored as a small text field rather than a numeric code so the
    audit/log surface stays human-readable. Empty string is permitted
    on legacy rows that pre-date this column — every new write goes
    through ``notify_cytova_service`` which always populates it.
    """
    CYTOVA = 'CYTOVA', 'Cytova'
    EMAIL = 'EMAIL', 'Email'
    SHARE_LINK = 'SHARE_LINK', 'Share link'
    MANUAL = 'MANUAL', 'Manual'


class SharedResultStatus(models.TextChoices):
    """Lifecycle of a shared-result row from the patient's point of view.

    ``ACTIVE``            — visible in the patient portal, downloadable.
    ``HIDDEN_BY_PATIENT`` — patient clicked "Remove from my space".
                            The row stays so the lab tenant retains
                            audit visibility, but it is excluded from
                            every patient-portal query and the file
                            download endpoint refuses to stream it.
    ``REVOKED``           — staff/admin revocation (e.g. wrong patient,
                            withdrawn share). Same effect on the
                            patient view as HIDDEN_BY_PATIENT but the
                            distinction is preserved for the audit
                            trail.
    """
    ACTIVE = 'ACTIVE', 'Active'
    HIDDEN_BY_PATIENT = 'HIDDEN_BY_PATIENT', 'Hidden by patient'
    REVOKED = 'REVOKED', 'Revoked'


class PatientSharedResult(models.Model):
    """
    Snapshot of a result a lab tenant has shared with a patient portal
    account. Lives in the public schema and intentionally has NO foreign
    key into any tenant model — see ``apps/patient_portal/__init__.py``
    for the full snapshot rationale. The lab tenant retains the
    canonical ``AnalysisRequest`` + ``AnalysisRequestReport`` rows in
    its own schema; this row is the patient-side view of "the lab said
    this result is mine".

    All fields are deliberately denormalised strings/dates: the lab
    name is captured at share time so a future tenant rename doesn't
    rewrite the patient's history; the request reference is what the
    patient should quote if they need to call the lab. Removing the
    underlying lab schema would not erase this record.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    patient_account = models.ForeignKey(
        PatientAccount,
        on_delete=models.CASCADE,
        related_name='shared_results',
    )
    source_type = models.CharField(
        max_length=10, choices=SharedResultSourceType.choices,
        default=SharedResultSourceType.DIRECT,
    )
    source_name = models.CharField(
        max_length=255,
        help_text='Snapshot of the lab name (or partner name) at share time.',
    )
    request_reference = models.CharField(
        max_length=64, blank=True, default='',
        help_text='Snapshot of the request public reference / number — '
                  'what the patient should quote when contacting the lab.',
    )
    request_date = models.DateField(null=True, blank=True)
    result_available_date = models.DateField(null=True, blank=True)
    created_by_lab = models.CharField(
        max_length=255, blank=True, default='',
        help_text='Optional snapshot of the staff member or lab identity '
                  'that initiated the share.',
    )
    status = models.CharField(
        max_length=20, choices=SharedResultStatus.choices,
        default=SharedResultStatus.ACTIVE, db_index=True,
    )
    created_at = models.DateTimeField(default=timezone.now, db_index=True)

    # ---- Immutable lab-side linkage ----
    # Captured at share time so the lab can later revoke its own
    # shares without taking a hard FK across schemas. ``source_*``
    # never changes after insert; updating here would silently rewrite
    # the patient's history, which the snapshot model exists to prevent.
    source_tenant_schema = models.CharField(
        max_length=64, blank=True, default='',
        help_text='Snapshot of the lab tenant schema name that produced '
                  'this share. Used by the lab-side revoke endpoint to '
                  'scope its lookup; never exposed to the patient.',
    )
    source_request_id = models.UUIDField(
        null=True, blank=True, db_index=True,
        help_text='Snapshot of the originating ``AnalysisRequest.id`` in '
                  'the lab tenant schema. Soft reference (no FK — the '
                  'tables live in different schemas).',
    )

    # ---- Patient-side activity counters (UX-friendly) ----
    first_viewed_at = models.DateTimeField(null=True, blank=True)
    last_downloaded_at = models.DateTimeField(null=True, blank=True)
    download_count = models.PositiveIntegerField(default=0)

    # ---- Revocation (lab side) ----
    revoked_at = models.DateTimeField(null=True, blank=True)
    revoked_by_lab = models.CharField(
        max_length=255, blank=True, default='',
        help_text='Snapshot of who/what triggered the revocation '
                  '(staff email or lab identity). Free text — the '
                  'patient never sees this.',
    )

    # ---- Notification status snapshot ----
    # Tri-state: '' (not attempted), 'SENT', 'FAILED'. Stays as a plain
    # string rather than a TextChoices to keep migrations cheap; the
    # set is small and stable enough to not justify the constant churn.
    email_notification_status = models.CharField(
        max_length=20, blank=True, default='',
    )
    email_notification_sent_at = models.DateTimeField(null=True, blank=True)

    # ---- Patient-facing report version metadata ----
    # The patient portal tracks a SEPARATE version line from the lab's
    # internal ``AnalysisRequestReport`` versions: a row exists here
    # only when the lab actively shared/notified that version. A lab
    # regenerating a report internally without sharing must NOT update
    # the patient's view — that invariant is enforced by the share
    # services (they are the only writers of these columns).
    #
    # ``report_version_number`` is the lab's internal version number
    # snapshotted at share time, kept here for traceability and the
    # patient-facing "Version N" badge. It is allowed to be NULL on
    # rows that pre-date this column — new writes always populate it.
    report_version_number = models.PositiveIntegerField(
        null=True, blank=True,
        help_text='Lab-side report version_number snapshotted at share time.',
    )
    report_generated_at = models.DateTimeField(
        null=True, blank=True,
        help_text='Snapshot of when the underlying lab report version was '
                  'generated. Distinct from ``shared_at`` because a version '
                  'may be generated long before it is actually shared.',
    )
    shared_at = models.DateTimeField(
        null=True, blank=True, db_index=True,
        help_text='When this version was actively shared/notified to the '
                  'patient. Coincides with ``created_at`` for new rows; '
                  'kept as a separate column so the patient versions API '
                  'has a semantic, channel-agnostic timestamp to expose.',
    )
    shared_channel = models.CharField(
        max_length=15, blank=True, default='',
        choices=PatientSharedChannel.choices,
        help_text='Which channel materialised this shared version. '
                  '``CYTOVA`` is the only writer in Phase 1.',
    )
    is_current_for_patient = models.BooleanField(
        default=True, db_index=True,
        help_text='True iff this is the latest version the lab shared with '
                  'this patient for this source request. Toggled to False '
                  'by the share service when a newer version is shared. '
                  'Independent of ``status`` — a row that is current for '
                  'the patient may still be REVOKED, in which case the '
                  'patient view filters it out anyway.',
    )

    class Meta:
        db_table = 'patient_portal_shared_result'
        verbose_name = 'Patient Shared Result'
        verbose_name_plural = 'Patient Shared Results'
        ordering = ['-created_at']
        indexes = [
            models.Index(fields=['patient_account', '-created_at']),
        ]

    def __str__(self) -> str:
        return (
            f'PatientSharedResult<account={self.patient_account_id} '
            f'source={self.source_name!r} ref={self.request_reference!r}>'
        )


class PatientSharedResultFile(models.Model):
    """
    File reference attached to a ``PatientSharedResult``.

    The ``file_token`` is an opaque, non-guessable identifier the
    patient portal will quote to a future download endpoint — it does
    NOT reuse the lab's per-tenant ``ResultAccessToken`` (which is a
    different concept with a 48h TTL and is bound to a tenant patient
    record). The file itself is NOT physically duplicated yet; the
    download endpoint (out of scope for this step) will resolve the
    token to the original tenant storage key and stream from there.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    shared_result = models.ForeignKey(
        PatientSharedResult,
        on_delete=models.CASCADE,
        related_name='files',
    )
    file_token = models.CharField(
        max_length=64, unique=True, db_index=True, editable=False,
        help_text='Opaque random reference used by the future patient '
                  'portal download endpoint.',
    )
    filename = models.CharField(
        max_length=255,
        help_text='Suggested download filename for the patient browser.',
    )
    # ``storage_key`` is the lab-side snapshot — historically the only
    # storage reference the patient portal carried. Kept for back-compat
    # so older rows still resolve via the download endpoint's fallback
    # (see ``storage_origin`` below). New rows additionally populate
    # ``patient_storage_key`` with a portal-owned copy of the PDF so the
    # patient's access doesn't depend on lab-side storage lifecycle.
    storage_key = models.CharField(
        max_length=500, blank=True, default='',
        help_text='Snapshot of the LAB-side file storage key. Internal — '
                  'patients never see this. Used as a fallback when the '
                  'patient-side copy is unavailable (e.g. legacy rows '
                  'or copy-time failure).',
    )
    patient_storage_key = models.CharField(
        max_length=500, blank=True, default='',
        help_text='Path of the portal-owned copy of the PDF. Created at '
                  'share time via ``copy_file``. Always preferred by the '
                  'download endpoint over the lab-side snapshot.',
    )
    storage_origin = models.CharField(
        max_length=10,
        choices=[('LAB', 'Lab'), ('PATIENT', 'Patient')],
        default='LAB', db_index=True,
        help_text='Which storage location backs this row. ``PATIENT`` '
                  'means the file was successfully copied into the '
                  'patient portal storage; ``LAB`` means the patient is '
                  'still served from the original tenant location '
                  '(legacy rows or copy-failure fallback).',
    )
    created_at = models.DateTimeField(default=timezone.now)

    class Meta:
        db_table = 'patient_portal_shared_result_file'
        verbose_name = 'Patient Shared Result File'
        verbose_name_plural = 'Patient Shared Result Files'

    def __str__(self) -> str:
        return f'PatientSharedResultFile<{self.filename}>'

    @property
    def effective_storage_key(self) -> str:
        """The storage key the download endpoint should actually open.
        Always prefers the patient-owned copy when present so a future
        lab-side cleanup never breaks the patient's access."""
        return self.patient_storage_key or self.storage_key


