class PatientPortalAuditLog(models.Model):
    """Append-only event log for the global patient portal.

    Lives in the public schema (not in any lab tenant). Distinct from
    the per-tenant ``apps.audit.AuditLog`` because the events recorded
    here belong to the global patient identity, not to a single lab —
    a patient-side download, hide, or share-revocation is meaningful
    across labs. The tenant audit log still gets its own row when
    appropriate (e.g. revoke writes both).

    Safety contract for ``metadata`` values
    --------------------------------------
    Callers MUST NOT include in the metadata dict:
      - PDF storage keys / URLs / file paths
      - download tokens or any opaque secret
      - patient PII (name, DOB, email, phone)
      - clinical / medical content

    Only IDs already known to both sides (``shared_result_id``,
    ``file_id``, ``source_request_reference``) plus public counters are
    safe. The ``apps.patient_portal.audit`` helper enforces a small
    set of allowed keys for each action.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    patient_account = models.ForeignKey(
        PatientAccount,
        on_delete=models.SET_NULL,
        null=True, blank=True,
        related_name='audit_events',
        help_text='Set null on account deletion so the audit row '
                  'survives — needed for forensic continuity even when '
                  'the actor account is gone.',
    )
    action = models.CharField(
        max_length=40, choices=PatientPortalAuditAction.choices, db_index=True,
    )
    entity_type = models.CharField(max_length=64)
    entity_id = models.UUIDField(null=True, blank=True, db_index=True)
    ip_address = models.GenericIPAddressField(null=True, blank=True)
    user_agent = models.CharField(max_length=500, blank=True, default='')
    metadata = models.JSONField(default=dict, blank=True)
    created_at = models.DateTimeField(default=timezone.now, db_index=True)

    class Meta:
        db_table = 'patient_portal_audit_log'
        verbose_name = 'Patient Portal Audit Log'
        verbose_name_plural = 'Patient Portal Audit Logs'
        ordering = ['-created_at']
        indexes = [
            models.Index(fields=['patient_account', '-created_at']),
            models.Index(fields=['action', '-created_at']),
        ]

    def __str__(self) -> str:
        return (
            f'PatientPortalAuditLog<{self.action} '
            f'patient={self.patient_account_id} at={self.created_at:%Y-%m-%d %H:%M}>'
        )


