class PatientTokenType(models.TextChoices):
    """``access`` / ``refresh`` mirror the simplejwt token classes so a
    log query can split the two trivially."""
    ACCESS = 'ACCESS', 'Access'
    REFRESH = 'REFRESH', 'Refresh'


class PatientOutstandingToken(models.Model):
    """One row per token we issue to a patient. Indexed on ``jti`` so
    the auth backend can resolve in O(1) on every authenticated
    request.

    We deliberately store ONLY the JWT ID (``jti``) — never the full
    token, never the signature. The plaintext token lives in the
    patient's browser; this row exists purely to gate the JTI behind
    a blacklist check on every request and to support session-wide
    revocation (logout-all, password change).
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    patient_account = models.ForeignKey(
        PatientAccount,
        on_delete=models.CASCADE,
        related_name='outstanding_tokens',
    )
    jti = models.CharField(
        max_length=64, unique=True, db_index=True,
        help_text='JWT ID claim — the only durable identifier we '
                  'store. Compared against the ``jti`` in the bearer '
                  'token on every request.',
    )
    token_type = models.CharField(
        max_length=10, choices=PatientTokenType.choices,
    )
    created_at = models.DateTimeField(default=timezone.now, db_index=True)
    expires_at = models.DateTimeField(db_index=True)
    user_agent = models.CharField(max_length=500, blank=True, default='')
    ip_address = models.GenericIPAddressField(null=True, blank=True)

    class Meta:
        db_table = 'patient_portal_outstanding_token'
        verbose_name = 'Patient Outstanding Token'
        verbose_name_plural = 'Patient Outstanding Tokens'
        indexes = [
            # Compound index supports the per-account lookup the
            # logout-all + password-change paths use, and lets a
            # cleanup query prune by expiry without scanning the
            # whole table.
            models.Index(fields=['patient_account', 'expires_at']),
        ]

    def __str__(self) -> str:
        return (
            f'PatientOutstandingToken<{self.token_type} '
            f'patient={self.patient_account_id} jti=…{self.jti[-6:]}>'
        )


class PatientBlacklistedToken(models.Model):
    """Marks an outstanding token as revoked. The auth backend
    rejects any token whose Outstanding row has a corresponding
    Blacklisted row.

    Modelled as a ``OneToOneField`` so the auth backend can resolve
    "is this token blacklisted?" in a single ``select_related`` join
    rather than a follow-up SELECT.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    token = models.OneToOneField(
        PatientOutstandingToken,
        on_delete=models.CASCADE,
        related_name='blacklist',
    )
    blacklisted_at = models.DateTimeField(default=timezone.now)

    class Meta:
        db_table = 'patient_portal_blacklisted_token'
